DECLARE
	NUM int:=0; 
BEGIN
	WHILE NUM<=5
	LOOP
		NUM := NUM+1;
		DBMS_OUTPUT.PUT_LINE(NUM);
		EXIT WHEN NUM = 3;
	END LOOP;
	
END;
